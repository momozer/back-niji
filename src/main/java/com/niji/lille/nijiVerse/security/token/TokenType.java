package com.niji.lille.nijiVerse.security.token;

public enum TokenType {

    /**
     *  énumération qui contient un seul type de jeton, à savoir BEARER
     */
    BEARER

    // QU'est-ce qu'un Token ?
    //Un jeton (token en anglais) est un code alphanumérique qui est utilisé pour identifier un utilisateur
    // de manière unique dans une application ou un système. Le type de jeton peut être utilisé pour différencier
    // différents types de jetons ou de méthodes d'authentification. Ici, le type de jeton est utilisé pour indiquer
    // que le jeton est un jeton Bearer, qui est utilisé dans le cadre de l'authentification HTTP Bearer.
}
